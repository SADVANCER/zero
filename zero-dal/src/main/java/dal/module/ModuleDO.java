package dal.module;

/**
 * @author ningcheng
 * @date 2017/9/30
 */
public class ModuleDO {

    private long id;
    private String name;
    private String note;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
